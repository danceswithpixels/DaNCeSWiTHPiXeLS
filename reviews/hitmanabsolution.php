Hitman: Absolution does not demand that you play its predecessors, but doing so does add emotional depth to our protagonist, Agent 47. By itself, Absolution tells an isolated story of one man's quest to protect an innocent girl. The storytelling is far from masterful, as its sole purpose is to give the player one target to kill after another as cohesively as possible.
<br><br>
The campaign spans across a dozen plus missions (20 are listed, but some are just cutscenes). Each mission is composed of 3-5 maps following a unified theme: a hotel, factory, suburb, etc. While there is no shortage of stages, many are poor in design. One major feature of any great stealth game is availability of options: multiple places to navigate and multiple ways to kill. Unfortunately, Hitman severely lacks the former. Oftentimes, a stage will offer three different routes to a target with two being heavily guarded. Consequently, the player is strongly encouraged to take the remaining path. Such imbalance discourages exploration and experimentation, two vital components to any stealth game.
<br><br>
New to gameplay are the cover system, Instinct mode, and a redesigned disguise system. Like other modern third-person shooters, Agent 47 can now take cover behind walls. He can also enter Instinct mode which slows down time, allowing the player to mark and execute enemies (like Splinter Cell: Conviction). Instinct points are earned by killing enemies, hiding bodies, and completing objectives. The new disguise system allows NPCs wearing the same outfit as 47 to see through his disguise. This mechanic forces the player to change into different outfits based on his or her surroundings.
<br><br>
The biggest faults of these new features are their inconsistencies. While in cover, 47 can incapacitate nearby enemies, but his reach is often unpredictable. Some times 47 will be able to take down enemies at some distance away, while other times, he will be confounded by someone standing inches from him. Even more frustrating is how easily 47 can be accidentally moved out of cover. The rate and chances of NPCs seeing through disguises also feel inconsistent, preventing the player from fully taking advantage of their behavior. As mechanics which you will be using many times throughout the game, these are huge issues!
<br><br>
Absolution's saving grace is its Contracts Mode, the closest a Hitman game has ever come to multiplayer. Here, players from around the world can create custom contracts by personally selecting up to three targets on any map and challenging other players to assassinate them under specific conditions, but not before the creator can prove it can be done himself. Contract completion is rewarded with currency used to buy and upgrade weapons. It is this mode that will keep fans coming back for more long after they have finished the campaign.
<br><br>
If it were not for Contract Mode, Hitman: Absolution would be relegated to a game just slightly above average and deserving of a score no higher than 3/5. The thrill and excitement of a stealth game is still there, but it is difficult to recommend Absolution when there are other games just as effective and arguably even better. While Absolution is not great, I suppose it is still worthy of the Hitman series.
<br><br>
Grade: 4/5

