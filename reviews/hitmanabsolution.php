<h1 style="text-align:center">Hitman: Absolution (Review)</h1>

I have nothing but fond memories of the Hitman series, which was the first to turn me into a fan of the stealth genre. Having beaten Blood Money years ago, I counted down the days until I would see a sequel. Six years later, Eidos finally managed to deliver, but was it worth the wait?
