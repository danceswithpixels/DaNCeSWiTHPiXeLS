<h1 style="text-align:center">Mark of the Ninja (Review)</h1>

Of the two major releases I have played from Klei Entertainment (Shank and Shank 2), both felt extremely underwhelming. It was by chance that I stumbled onto Mark of the Ninja, and having noticed its surprisingly high review scores, I made my purchase with cautious optimism. It was marketed as a stealth game, standing in stark contrast with the action-packed gameplay familiar to the Shank series. If Shank was so mediocre, that had to be a good thing, right?
<br><br>
