By any previous indication, Klei Entertainment never seemed to have a strong sense for storytelling. Fortunately, they managed to pull through this time around. Mark of the Ninja revolves around... well, a ninja. But with a paranormal twist, he is endowed with supernatural powers derived from his tattoos. Like all things awesome, however, there is a catch! As it turns out, the markings will drive its bearers insane and thus, they are fated to commit seppuku once their services are no longer required. The plot follows our unnamed protagonist as he protects his village from militants, all the while questioning his fate. There are several surprises that keep the narrative interesting, but by no means is it revolutionary.
<br><br>
Even though the story can stand proudly on its own, it is still overshadowed by the innovative gameplay. It should be known that this game strongly encourages a stealthy and tactical approach to all situations. It is rarely, if ever, advised to tackle enemies head on. With that said, there is still an enormous variety of ways with which the player can accomplish his or her tasks.
<br><br>
The campaign spans a dozen brilliantly designed levels, each filled with numerous passages to any one location. Aside from the obvious paths, players may also traverse through sewer ducts, rooftops, and vents. There are walls you can scale and ceilings you can swing across. Even without the array of gadgets and skills at your disposal, each map alone offers numerous ways to travel, allowing the player to differentiate each replay as he so chooses.
<br><br>
But even more astounding are the combinations of outfits and skills available. There are six unlockable outfits, each with their own unique statistics. The game also offers a sizeable selection of distraction items such as smoke bombs, poison darts, and more! And if that were not enough, there are upgradeable passive skills such as reduced movement noise and the ability to assassinate while in cover. Despite being limited to stealth gameplay, Mark of the Ninja still maintains the freedom for players to choose how they want to play.
<br><br>
Other notable features include the game's art and animation, hidden puzzles, and "sound mechanic." Of the three, it is the latter two that I would like to briefly gloss over. Hidden in each level is an entrance (depicted as a gong) to a puzzle stage. These puzzles task the player with reaching an item by moving boxes around in order to thwart traps and open passages. While an unnecessary addition, these segments are a welcome break from all the sneaking around. Lastly, the game visually depicts sounds using emanating circles. This allows players to both gauge how noisy he is being and predict enemy movement. It is this mechanic that makes the game feel so consistent and accessible to casual players.
<br><br>
I am not certain if Klei Entertainment has hit their stride or if it was just a stroke of luck. Nonetheless, Mark of the Ninja is worth every penny of its asking price and I sincerely hope its successors will be too.
<br><br>
Grade: 5/5

