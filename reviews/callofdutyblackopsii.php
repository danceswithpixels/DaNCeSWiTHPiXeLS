<h1 style="text-align:center">Call of Duty: Black Ops II (Review)</h1>

For readers looking for an in-depth review of Black Ops II's multiplayer, I am sorry to disappoint. My knowledge in that area is minimal, and as far as I am concerned, nothing has changed since the last iteration (save for some new weapons and maps). It is still a fun and solid experience rivaled only by few others, but my true qualifications as a reviewer lie mainly in single player.
