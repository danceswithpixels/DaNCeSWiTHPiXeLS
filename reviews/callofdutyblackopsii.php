Despite Black Ops II's static multiplayer component, the game still shows effort to break new ground through its ambitious campaign. As usual, the plot involves some conspiracy to incite global warfare, but this time it is one worth revisiting even after the credits have rolled. Since the first Black Ops, Treyarch (one of three Call of Duty developers alongside Infinity Ward and Sledgehammer Games) has shown a knack for storytelling. Their talents are still clearly visible here through their complex narrative cleverly woven together by events of the Cold War and Treyarch's vision of the year 2025.
<br><br>
For the first time in Call of Duty history, the campaign offers branching storylines that are shaped by missions with multiple endings (determined by whether or not objectives were achieved) and choices made by the player. In the case of the latter, decisions do not boil down to choosing from a list, but are made with gameplay mechanics, be it interacting with the environment or choosing which limb to shoot. Often times, you will play through these moments without even realizing that alternate options existed. That is what makes Black Ops II great: it encourages players to reconsider their actions. Ultimately, the secrets found and choices made will determine which of the five ways the game will end.
<br><br>
Also new is a tower defense-like mini game in which the player must direct troops against waves of enemies in bird's-eye view. At any moment, the player may assume first person control of a friendly unit on the battlefield and play as normal. These missions, known as Strike Force missions, serve as supplements to the main story, unlocking themselves when certain conditions are met. They are a welcome addition and help immensely to vary the gameplay.
<br><br>
As proud as I am of Treyarch, especially for their contributions to the single player, there is still plenty of room for improvement. In vehicle segments, handling feels extremely sluggish and the unrealistic physics breaks all immersion. The script is filled with clich√©s and the absurdity of some missions should have been toned down. Stages set in 2025 introduce several game-changing gadgets with promising potential that never reappear. But the biggest shortcoming of all is the fact that Black Ops II feels like a glorified game of Whack-a-Mole where enemies repeatedly pop out for you to shoot.
<br><br>
Multiplayer and single player aside, there is still the topic of Zombie Mode. The new features here are a separate campaign and a new 8 player game type. While Treyarch's efforts are appreciated, the final product is all but satisfying. The "campaign" merely consists of ordinary survival maps strung together by cutscenes and fighting zombies with an army makes an already dull experience even duller.
<br><br>
It is refreshing to see a developer cater to the single player fans for a title with a track record for being predominantly multiplayer. In doing so, Treyarch has raised the bar for first person shooter narratives. Regardless of what comes from Infinity Ward next year, I can honestly say that I eagerly await Treyarch's next release, knowing very well that they are not one to shy away from being different.
<br><br>
Grade: 4/5

